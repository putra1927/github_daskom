    <h2>Apa itu framework?</h2>
    <p> Sesuai dengan namanya sendiri, framework adalah kerangka kerja untuk mengembangkan aplikasi berbasis website maupun desktop. Kerangka kerja disini sangat membantu developer dalam menuliskan sebuah dengan lebih terstruktur dan tersusun rapi.
Kerangka kerja diciptakan untuk mempermudah kinerja dari programmer. Sehingga, seorang programmer tidak perlu untuk menuliskan kode secara berulang – ulang. Karena di dalamnya sendiri anda hanya perlu menyusun komponen – komponen pemrograman saja. </p>
    <h2>Pengertian?</h2>
    <p> pengertian framework adalah fondasi dengan tingkat kerumitan yang ditentukan dan dapat diperluas oleh pemrogram dengan menggunakan kode mereka sendiri termasuk kompiler, juru bahasa, atau API. Secara umum, framework menyediakan lingkungan yang memfasilitasi jenis pemrograman tertentu untuk proyek pengembangan software.
    
    <h2>Fungsi Framework?</h2>
    <p> Framework memiliki fungsi sebagai berikut ini:

Mempercepat proses pembuatan aplikasi, baik aplikasi berbasis desktop, mobile atau web.
Membantu para developer dalam perencanaan, pembuatan, dan pemeliharaan sebuah aplikasi.
Aplikasi yang dihasilkan menjadi lebih stabil dan handal karena framework sudah melalui proses uji, baik uji stabilitas dan kehandalannya.
Memudahkan para developer dalam membaca kode program dan lebih mudah dalam mencari bugs.
Memiliki tingkat keamanan yang lebih karena framework telah mengantisipasi cela-cela keamanan yang mungkin timbul.
Memudahkan developer dalam mendokumentasikan aplikasi-aplikasi yang sedang dibangun. <p>
<h2>Jenis Framework</h2>
<p> Inilah jenis-jenis Framework yang paling populer di dunia:<p>

<p> * CodeIgniter
CodeIgniter merupakan salah satu Framework yang dikembangkan oleh Rick Ellis berdasarkan script PHP. Selanjutnya CodeIgniter disempurnakan lagi oleh British Columbia Institute of Technology. Saat ini CodeIgniter sudah dikembangkan hingga versi yang ke-4.<p>

<p> * Laravel
Laravel merupakan salah satu web framework yang cukup terkenal dibandingkan yang lainnya. Framework yang satu ini diciptakan oleh Taylor Otwell yang hingga kini terus bekerja keras untuk dapat membangun Laravel menjadi mudah dan nyaman digunakan. Laravel pun memiliki dokumentasi yang lengkap dan jelas untuk memudahkan para web developer dalam membangun aplikasi web mereka.<p>

<P> * Yii
Web Framework satu ini mempunyai slogan “The Fast, Secure, and Professional PHP Framework.“ Yii juga memiliki dokumentasi dan dukungan komunitas yang kuat. Framework yang satu ini juga memiliki fitur-fitur unggulan, seperti sistem widget, filter, dan RBAC. Keunggulan-keunggulan tersebut  tentu saja membuat Yii menjadi salah satu web framework yang perlu diperhitungkan. <p>
